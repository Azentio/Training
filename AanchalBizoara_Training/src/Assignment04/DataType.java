package Assignment04;

import java.util.Scanner;

public class DataType {

	public static void main(String[] args) {
		byte b = 127; // 3 digits
		short s = 32767; // 5 digits
		long l = 214746847; // 9 digits
		int i = 2147264891; // 10 digits

		System.out.println("Most used data types in JAVA are:");
		System.out.println("Byte is of length 3 digits max: " + b);
		System.out.println("Short is of length 5 digits max: " + s);
		System.out.println("Long is of length 9 digits max: " + l);
		System.out.println("Integer is of length 10 digits max: " + i);

	}

}
